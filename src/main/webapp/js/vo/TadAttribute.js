function TadAttribute()  {
	var self = this;
	
	self.id = '';
	self.functionId = '';
	self.name = '';
	self.type = '';
	self.label = '';
	self.required = false;
	self.isUnique = false;
	self.searchable = false;
	self.optionText = '';
	self.dataSourceName = '';
	self.searchTextDataSourceName = '';
	self.shownInTable = true;
}